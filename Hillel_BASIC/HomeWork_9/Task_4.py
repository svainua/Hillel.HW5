# Дано два списки однакової довжини.
# Необхідно створити з них словник таким чином, щоб елементи першого списку були ключами,
# а елементи другого відповідно значеннями нашого словника.

list_1 = [2, 4, 6, 8]
list_2 = [3, 5, 7, 9]

my_dict = {key: value for key, value in zip(list_1, list_2)}

print(my_dict)

